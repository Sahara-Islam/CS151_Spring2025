/* Examine the following program: */
// This program uses variables and literals.
#include <iostream>

using namespace std;

int main()
{
    int little;
    int big;
    little = 2;
    big = 2000;
    cout << "The little number is " << little << endl;
    cout << "The big number is " << big << endl;
    return 0;
}

